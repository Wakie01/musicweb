package pojo;

public class UserInfo {
    private String uname;
    private int tupload;
    private int ugrade;
    private double uroom;
    private double urmspace;

    public UserInfo() {
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public int getTupload() {
        return tupload;
    }

    public void setTupload(int tupload) {
        this.tupload = tupload;
    }

    public int getUgrade() {
        return ugrade;
    }

    public void setUgrade(int ugrade) {
        this.ugrade = ugrade;
    }

    public double getUroom() {
        return uroom;
    }

    public void setUroom(double uroom) {
        this.uroom = uroom;
    }

    public double getUrmspace() {
        return urmspace;
    }

    public void setUrmspace(double urmspace) {
        this.urmspace = urmspace;
    }
}
